package com.train;
abstract interface Train
{
	public void travelling();
	public void travel();
	
}
abstract class Narsapur implements Train
 {
	public void travelling()
	 {
		 System.out.println("from station is Nsp");
		 System.out.println("to station is Hyd");
	 }
	 
 }
class VishakhaExp extends Narsapur
 {
	public void travel()
	 {
		 System.out.println("from station is Vizag");
		 System.out.println("to station is Araku");
	 }
	 
 }
public abstract class DemoTrain1{

	public static void main(String[] args) {
		VishakhaExp  ob=new VishakhaExp();
	    ob.travelling();
	    ob.travel();
	}

}
